import Foundation
import UIKit

extension UIColor {
    class var textPink: UIColor {
        return UIColor(named: "text_pink")!
    }
    class var iconGray: UIColor {
        return UIColor(named: "icon_gray")!
    }
    class var searchGray: UIColor {
        return UIColor(named: "search_gray")!
    }
}
